//^ @protected: Search(const string)
bool Account::Search(const string target)
{
    //& @note: file verification
    ifstream savefile("resources/.cache/.cache.csv");
    if (!Verify(savefile))
        return false;

    //& @note: file parsing
    string line;
    while (getline(savefile, line))
    {
        stringstream row(line);
        string name, key;

        //* @note: search hit
        if (getline(row, name, ',') && getline(row, key))
        {
            username = name;
            passkey = key;
            savefile.close();

            return true;
        }
    }

    //! @note: search miss
    savefile.close();
    return false;
}
