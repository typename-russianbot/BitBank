bool Account::Save(void)
{
    ofstream savefile("resources/.cache/.cache.csv", ios::app);
    if (!Verify(savefile))
        return false;
    else
    {
        savefile << username << "," << passkey << endl;
        savefile.close();
        return true;
    }

    savefile.close();
    return false;
}
