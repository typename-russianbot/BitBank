//~ @defgroup: Overloads
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: operator<<(ostream&, const Data&)
ostream &operator<<(ostream &out, const Transaction &data)
{
    out << "| Transaction | " << data.title << " |" << endl
        << "Description: \t" << data.description << endl
        << "Type: \t\t" << data.type << endl
        << "Date: \t\t" << data.date << endl
        << "Amount: \t" << data.amount << endl;

    return out;
}
