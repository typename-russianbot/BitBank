#include "../../includes/data/transaction.h"
#include <termios.h>

//~ @defgroup: Resources
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: Data(const Type, const string, const string, const string, const signed int)
Transaction::Transaction(const Type nType, const Date nDate, const string nDescription, const double nAmount) : type(nType), date(nDate), description(nDescription), amount(nAmount) { return; }
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: Data(const Data&)
Transaction::Transaction(const Transaction &data) : type(data.type), date(data.date), description(data.description), amount(data.amount) { return; }
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: ~Data(void)
Transaction::~Transaction(void) { return; }
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//~ @defgroup: Mutators
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: getType(void)
const Type Transaction::getType(void)
{
    return type;
}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: getDate(void)
const Date Transaction::getDate(void)
{
    return date;
}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: getDescription(void)
const string Transaction::getDescription(void)
{
    return description;
}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: getAmount(void)
const double Transaction::getAmount(void)
{
    return amount;
}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//~ @defgroup: Accessors
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: setType(const Type)
void Transaction::setType(const Type nType)
{
    type = nType;
    return;
}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: setDate(const Date)
void Transaction::setDate(const Date nDate)
{
    date = nDate;
    return;
}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: setDescription(const string)
void Transaction::setDescription(const string nDescription)
{
    description = nDescription;
    return;
}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: setAmount(const double)
void Transaction::setAmount(const double nAmount)
{
    amount = nAmount;
    return;
}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//~ @defgroup: Overloads
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: operator<<(ostream&, const Data&)
ostream &operator<<(ostream &out, const Transaction &data)
{
    out << data.date << "\t||\t" << data.type << " \t||\t\t" << data.amount << "\t\t||\t" << data.description;

    return out;
}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: operator=(const Data&)
Transaction &Transaction::operator=(const Transaction &other)
{
    this->type = other.type;
    this->date = other.date;
    this->description = other.description;
    this->amount = other.amount;
    return *this;
}
