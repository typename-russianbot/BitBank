//~ @defgroup: Functions
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//* @public: setDate(const string)
void Date::setDate(const string input)
{
    int slashPos[2] = {0, 0};
    //& @def: string format in .csv -- month/day/year --
    for (int i = 0; i < input.size() - 1; i++)
    {
        if (input[i] == '/' && slashPos[0] == 0)
            slashPos[0] = i;
        else if (input.c_str()[i] == '/' && (slashPos[0] != 0 && slashPos[1] == 0))
            slashPos[1] = i;
    }

    string m, d, y;
    for (int i = 0; i < slashPos[0]; i++)
    {
        m += input[i];
    }

    for (int i = slashPos[0] + 1; i < slashPos[1]; i++)
    {
        d += input[i];
    }

    for (int i = slashPos[1] + 1; i <= input.size(); i++)
    {
        y += input[i];
    }

    month = stoi(m);
    day = stoi(d);
    year = stoi(y);
    return;
}
